class Calculator(object):

    def add(self, a, b):
        return a + b

    def subtract(self, a, b):
        return a - b

    def multiply(self, a, b):
        return a * b

    def divide(self, a, b):
        return a / b

    def modulus(self, a, b):
        return a % b

    def squared(self, a):
        return a ** 2

    def cubed(self, a):
        return a ** 3

    def power(self, a, b):
        return a ** b

    def sqrt(self, a):
        return (a**0.5)

    def root(self, a, b):
        return (a**(1/b))

    def abs(self, a):
        if a <= 0:
            return -a
        else:
            return a
